import mongoose from "mongoose";

const UserManagementSchema = new mongoose.Schema({
  Name: {
    type: String,
    required: true,
  },
  Email: {
    type: String,
    required: true,
    unique: true,
  },
  Password: {
    type: String,
    required: true,
  },
});

export default mongoose.model("UserManagement", UserManagementSchema);
